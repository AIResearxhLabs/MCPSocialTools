#!/bin/bash

# Local Docker Desktop Deployment Script for MCPSocial Service
# This script builds and runs the service locally using Docker Desktop

set -e

# --- Configuration ---
IMAGE_NAME="mcpsocial"
CONTAINER_NAME="mcpsocial-app"
HOST_PORT=3001
CONTAINER_PORT=3001
SERVICE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó"
echo "‚ïë       MCPSocial Local Docker Desktop Deployment          ‚ïë"
echo "‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù"
echo ""

# --- Pre-flight Checks ---
echo "üîç [1/6] Pre-flight checks..."
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"

if ! command -v docker &> /dev/null; then
    echo "‚ùå Error: Docker is not installed or not in the system's PATH."
    echo "   Please install Docker Desktop and try again."
    exit 1
fi

if ! docker info &> /dev/null; then
    echo "‚ùå Error: Docker daemon is not running."
    echo "   Please start Docker Desktop and try again."
    exit 1
fi

echo "‚úÖ Docker is running"
echo ""

# --- Environment Variable Check ---
echo "üîê [2/6] Environment variable configuration..."
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"

ENV_FILE="${SERVICE_DIR}/.env"

if [ ! -f "$ENV_FILE" ]; then
    echo "‚ö†Ô∏è  Warning: .env file not found."
    echo "   Creating .env file from .env.example..."
    cp "${SERVICE_DIR}/.env.example" "$ENV_FILE"
    echo ""
    echo "üìù Please edit the .env file and add your API keys:"
    echo "   - OPENAI_API_KEY"
    echo "   - FACEBOOK_ACCESS_TOKEN"
    echo "   - INSTAGRAM_ACCESS_TOKEN"
    echo ""
    read -p "Press Enter after updating the .env file to continue..."
fi

# Source the .env file to check required variables
if [ -f "$ENV_FILE" ]; then
    export $(cat "$ENV_FILE" | grep -v '^#' | xargs)
    
    # Check for required variables
    if [ -z "$OPENAI_API_KEY" ] || [ "$OPENAI_API_KEY" = "your_openai_api_key_here" ]; then
        echo "‚ùå Error: OPENAI_API_KEY not set in .env file"
        exit 1
    fi
    
    echo "‚úÖ Environment variables loaded"
else
    echo "‚ùå Error: .env file not found"
    exit 1
fi

echo ""

# --- Clean up old container ---
echo "üßπ [3/6] Cleaning up existing containers..."
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"

if [ "$(docker ps -q -f name=$CONTAINER_NAME)" ]; then
    echo "Stopping running container..."
    docker stop $CONTAINER_NAME
fi

if [ "$(docker ps -aq -f name=$CONTAINER_NAME)" ]; then
    echo "Removing old container..."
    docker rm $CONTAINER_NAME
fi

echo "‚úÖ Cleanup complete"
echo ""

# --- Prune old images ---
echo "üóëÔ∏è  [4/6] Pruning dangling images..."
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
docker image prune -f > /dev/null
echo "‚úÖ Prune complete"
echo ""

# --- Build the Docker image ---
echo "üê≥ [5/6] Building Docker image..."
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo "Building from: ${SERVICE_DIR}"

docker build -t "${IMAGE_NAME}:latest" "$SERVICE_DIR"

echo "‚úÖ Build complete"
echo ""

# --- Run the container ---
echo "üöÄ [6/6] Starting container..."
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"

docker run -d \
    -p $HOST_PORT:$CONTAINER_PORT \
    --name $CONTAINER_NAME \
    --env-file "$ENV_FILE" \
    "$IMAGE_NAME:latest"

echo "‚úÖ Container started"
echo ""

# --- Deployment Summary ---
echo "‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó"
echo "‚ïë            üéâ Local Deployment Complete!                  ‚ïë"
echo "‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù"
echo ""
echo "Service Information:"
echo "  ‚Ä¢ Container: $CONTAINER_NAME"
echo "  ‚Ä¢ Port: $HOST_PORT"
echo "  ‚Ä¢ Image: $IMAGE_NAME:latest"
echo ""
echo "Access Points:"
echo "  ‚Ä¢ API Capabilities: http://localhost:$HOST_PORT/api/capabilities"
echo "  ‚Ä¢ MCP Tools: http://localhost:$HOST_PORT/mcp/tools"
echo ""
echo "Useful Commands:"
echo "  ‚Ä¢ View logs:        docker logs -f $CONTAINER_NAME"
echo "  ‚Ä¢ Stop service:     docker stop $CONTAINER_NAME"
echo "  ‚Ä¢ Restart service:  docker restart $CONTAINER_NAME"
echo "  ‚Ä¢ Remove service:   docker stop $CONTAINER_NAME && docker rm $CONTAINER_NAME"
echo ""
echo "‚è≥ Waiting for service to be ready..."
sleep 3

# Test if service is responding
if curl -s http://localhost:$HOST_PORT/api/capabilities > /dev/null 2>&1; then
    echo "‚úÖ Service is responding!"
    echo ""
    echo "üîó Try it now:"
    echo "   curl http://localhost:$HOST_PORT/api/capabilities | jq"
else
    echo "‚ö†Ô∏è  Service might still be starting up. Check logs with:"
    echo "   docker logs -f $CONTAINER_NAME"
fi

echo ""
